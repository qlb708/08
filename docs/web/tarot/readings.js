function _getDominantElement(elements) {
  const counts = {};
  elements.forEach(e => { counts[e] = (counts[e] || 0) + 1; });
  let max = 0, dominant = elements[0];
  for (const [elem, count] of Object.entries(counts)) {
    if (count > max) { max = count; dominant = elem; }
  }
  return dominant;
}

