/* ========================================
   根据三张牌生成完整解读
   返回：cardReadings, insight (合并后的综合解读), actionAdvice, wisdom
   ======================================== */
function generateReading(cards) {
  const positions = ['past', 'present', 'future'];
  const positionLabels = ['过去', '现在', '未来'];

  // 1. 单牌解读
  const cardReadings = cards.map((card, i) => {
    const reading = CARD_READINGS[card.id];
    if (!reading) {
      return {
        position: positionLabels[i],
        name: card.name,
        nameEn: card.nameEn,
        color: card.color,
        image: card.image,
        interpretation: `${card.name}出现在${positionLabels[i]}的位置，象征着${card.meaning}。它提醒你在这个阶段保持觉察和行动力。`,
        advice: '保持觉察，在变化中寻找机会'
      };
    }
    return {
      position: positionLabels[i],
      name: card.name,
      nameEn: card.nameEn,
      color: card.color,
      image: card.image,
      interpretation: reading[positions[i]],
      advice: reading.advice
    };
  });

  // 2. 元素分析 + 整体流动 = 合并为一段综合解读 (insight)
  const elements = cards.map(c => c.element);
  const elementAnalysis = _getElementCombo(elements);
  const flowNarrative = _generateOverallFlow(cards);
  const insight = flowNarrative + '\n\n' + elementAnalysis;

  // 3. 行动建议 — 职场化
  const actionAdvice = _generateActionAdvice(cards, elements);

  // 4. 塔罗智慧
  const wisdom = _getRandomWisdom();

  return {
    cardReadings,
    insight,
    actionAdvice,
    wisdom
  };
}

function _getElementCombo(elements) {
  // 检查三同
  const tripleKey = elements.join('-');
  if (ELEMENT_COMBOS[tripleKey]) {
    return ELEMENT_COMBOS[tripleKey];
  }

  // 检查双元素组合
  const uniqueElements = [...new Set(elements)].sort();
  if (uniqueElements.length === 1) {
    const comboKey = `${uniqueElements[0]}-${uniqueElements[0]}-${uniqueElements[0]}`;
    if (ELEMENT_COMBOS[comboKey]) return ELEMENT_COMBOS[comboKey];
  } else if (uniqueElements.length === 2) {
    let comboKey = uniqueElements.join('-');
    if (ELEMENT_COMBOS[comboKey]) return ELEMENT_COMBOS[comboKey];
    comboKey = uniqueElements.reverse().join('-');
    if (ELEMENT_COMBOS[comboKey]) return ELEMENT_COMBOS[comboKey];
  }

  // 通用三元素组合
  const elementNames = {
    fire: '行动力', water: '人际智慧', light: '商业眼光', wind: '战略思维', shadow: '深度洞察'
  };
  const names = [...new Set(elements)].map(e => elementNames[e] || e);
  return `你的牌阵中${names.join('、')}三种能量交织，构成了一个多维度的力量矩阵。这意味着你当前需要在不同维度间灵活切换，而不是单点突破。善用你的多面性，它是你在复杂环境中的核心优势。`;
}

