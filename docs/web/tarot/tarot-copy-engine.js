window.generateTravelCopy = function(cards, destName, theme) {
  function pick(arr) { return arr && arr.length ? arr[Math.floor(Math.random() * arr.length)] : ''; }
  var card0 = cards[0]||{}, card1 = cards[1]||{}, card2 = cards[2]||{};
  function getCopy(card) {
    var db = window.CARD_COPY||{};
    var e = db[card.nameEn]; if(!e) return null;
    return card.reversed ? e.reversed : e.upright;
  }
  var copy0 = getCopy(card0), copy1 = getCopy(card1);
  var destC = (window.DEST_COPY||{})[destName]||{};

  // 第一行：标题
  var title = '你抽到「'+(card0.name||'')+'」「'+(card1.name||'')+'」「'+(card2.name||'')+'」。';

  // 第二段：牌义描述（图片模板风格）
  var stateLine = (copy0 && copy0.state && copy0.state.length) ? pick(copy0.state) : '';
  var needLine  = (copy1 && copy1.need  && copy1.need.length)  ? '而「'+card1.name+'」提醒你：' + pick(copy1.need) : '';
  var cardBlock = [stateLine, needLine].filter(Boolean).join('\n');

  // 第三段：目的地推荐
  var destLine = '所以这次，命运推荐你去：'+destName+'。';

  // 第四段：目的地理由
  var reasonLine = (destC.reason && destC.reason.length) ? pick(destC.reason) : '';

  // 第五段：处方用法
  var dosageLine = (destC.dosage && destC.dosage.length) ? pick(destC.dosage) : '';

  var body = [cardBlock, destLine, reasonLine, dosageLine].filter(Boolean).join('\n\n');

  return { title: title, body: body };
};

